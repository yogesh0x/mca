public class loop2 { 
	public static void main ( String args []) { 
	int x, y, r = 7 ; 
for ( x = 0 ; x < r ; x++) { 
	for ( y = 2*( r - x); y >= 0; y--) { 
		System.out.print (" "); 
	} 
for ( y = 0 ; y <= x ; y++) { 
	System.out.print("*"); 
} 
	System.out.println(""); 

     }
   }
 }

